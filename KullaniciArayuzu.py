        result = veri.selectStok(talep)

        if (int(result[0]) == None):
            self.hataEkrani2.setText("Uzgunuz. Uretici firma böyle bir ürün üretmemekte.")
        elif (int(result[0]) < miktar):
            self.hataEkrani2.setText(
                "Uzgunuz. Stok eksikliğinden dolayı işleminiz gerçekleşmemekte. Kısa süre sonra tekrar deneyin.")
        elif (int(result[0]) >= miktar):
            self.hataEkrani2.setText("İsleminiz gerceklesmistir.")
            veri.insertSiparis(id, adres, talep, miktar)
            result2 = veri.selectMinFiyat(talep)
            k = 0
            while (miktar > 0):
                if (result2[k][1] > miktar):
                    veri.updateStok(result2[k][0], miktar)
                    miktar = 0
                else:
                    veri.updateStok(result2[k][0], result2[k][1])
                    miktar = miktar - result2[k][1]
                k = k + 1

