            for i in result:
                stokKontrol.append(veri.bilesenKontrol(i[1]))

            k = 0
            kontrol = True

            while (kontrol == True and len(stokKontrol) > k):
                print(result[k][2], stok, stokKontrol[k])

                if (result[k][2] * stok > stokKontrol[k]):
                    kontrol = False

                k = k + 1

            if (kontrol == True):

                id = veri.KimyasalUrunInsert(name)
                veri.gelistirilenUrunInsert(name, stok)
                veri.urunInsert(name, tarih, rafOmru, id, stok)

                for i in result:
                    veri.hammaddeDusme(i[1], i[2] * stok, id)

                veri.setIscilik(id, stok)

            else:
                error = ("Yeteri kadar hammadde bulunmamakta.Hammadde satin aliniz.")
                self.hataEkrani.setText(error)

